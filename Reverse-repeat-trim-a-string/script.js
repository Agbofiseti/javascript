console.log('reverse, repeat and trim a string')


// reverse a string
const example = 'abdulateef'
const reverseString = example.split('').reverse().join('')
console.log(reverseString)

// repeat string

const example2 = 'supernatural'
const repeatString = example2.repeat(4)
console.log(repeatString)

// trim

const example3 = '  hello, world'
const trim = example3.trim()
console.log(trim)